void main(List<String> args) {
  /**
   * Dart Operators:
   * ==============================
   * [01]: Assignment Operators
   * Assignment operators are used to assign value to a variable.
   * - [ = ]    => assignment Operator          => age = 30;
   * - [ += ]   => add and assign               => age += 5; // 30 + 5 = 35
   * - [ -= ]   => subtract and assign          => age -= 5; // 30 - 5 = 25
   * - [ *= ]   => multiply and assign          => age *= 2; // 30 * 2 = 60
   * - [ /= ]   => divide and assign            => age /= 2; // 30 / 2 = 15
   * - [ ~/= ]  => divide and assign (integer)  => age ~/= 7;
   * - [ %= ]   => mod and assign               => age %= 7; // 30 / 7 = 28 - 30 = 2
   * ==============================
   * [02]: Arithmetic Operators
   * Arithmetic Operators are used to perform arithmetic operations
   * [ + ]      => Addition                             => 5 + 5 = 10
   * [ - ]      => Subtraction                          => 5 - 5 = 0
   * [ * ]      => Multiply                             => 5 * 5 = 25
   * [ / ]      => Division                             => 5 / 5 = 1
   * [ ~/ ]     => Division and return integer value    => 5 ~/ 3 = 1.6666 = 1
   * [ % ]      => Modulus                              => 5 % 3 = 5 / 3 = 1 = 3 + 2 = 5
   * ==============================
   * [03]: Unary Operators
   * Increment and decrement operators:
   * [ ++[prefix] ]   => the value after increment      => ++a
   * [ [prefix]++ ]   => The value before increment     => a++
   * [ --[prefix] ]   => the value after decrement      => --a
   * [ [prefix]-- ]   => The value before decrement     => a--
   * ==============================
   * [04]: Comparison Operators
   * Comparison Operators are used evaluate a comparison between two operands.
   * [ > ]            => Greater than                   => a > b
   * [ < ]            => Less than                      => a < b
   * [ >= ]           => Greater than or equal to       => a >= b
   * [ <= ]           => Less than or equal to          => a <= b
   * [ == ]           => Is equal to                    => a == b
   * [ != ]           => Is not equal to                => a != b
   * ==============================
   * [05]: Logical Operators
   * [ && ]           => return true if all             => a == 5 && b > 3 ( if both true return true ) 
   * [ || ]           => return true if any is true     => a == 5 || b > 3 ( if any expression is true return true )
   * [ !  ]           => Not                            => a != 5
   * ==============================
   * [06]: Type test Operators
   * The Type test operators are used for checking types at runtime.
   * [ as ]         => Typecast
   * [ is ]         => True if the object has the specified type      => // age is int : true // check if age is integer
   * [ is! ]        => False if the object has the specified type     => // age is! int : false // check if age is not integer
   * ==============================
  */
}
